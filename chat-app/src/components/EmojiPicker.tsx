export default function EmojiPicker({ isOpen, onClose, onEmojiSelect }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState("smileys");

  if (!isOpen) return null;

  return (
    <>
      <div className="emoji-backdrop" onClick={onClose} />
      <div className="emoji-picker">
        <div className="emoji-categories">
          {Object.entries(emojiCategories).map(([key, category]) => (
            <button
              key={key}
              className={`category-btn ${activeCategory === key ? "active" : ""}`}
              onClick={() => setActiveCategory(key)}
            >
              {category.name}
            </button>
          ))}
        </div>
        
        <div className="emoji-grid">
          {emojiCategories[activeCategory as keyof typeof emojiCategories].emojis.map((emoji, index) => (
            <button
              key={index}
              className="emoji-btn"
              onClick={() => {
                onEmojiSelect(emoji);
                onClose();
              }}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </>
  );
}
